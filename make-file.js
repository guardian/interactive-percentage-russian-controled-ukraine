const upload = async (body, filename, params = {}) => {
    let defaultParams = {
      Bucket: 'gdn-cdn/isw',
      ACL: 'public-read',
      ContentType: `application/${params.type}`,
      CacheControl: 'no-cache',
      Body: body,
      Key: filename,
    }
  
    return s3.upload(defaultParams).promise()
}

const getFileList = async (dirName) => {

    let files = [];

    const items = await fs.promises.readdir(dirName, { withFileTypes: true })

    for (const item of items) {
        if (item.isDirectory()) {
            files = [
                ...files,
                ...(await getFileList(`${dirName}/${item.name}`)),
            ];
        } else {
            files.push(`${dirName}/${item.name}`);
        }
    }

    return files
}

